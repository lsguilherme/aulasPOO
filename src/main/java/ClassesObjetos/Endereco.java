package ClassesObjetos;

public class Endereco {
    String rua;
    int numero;
    String bairro;
    String cidade;
    
    public Endereco(){}

    public Endereco(String rua, int numero, String bairro, String cidade){
        this.rua = rua;
        this.numero = numero;
        this.bairro = bairro;
        this.cidade = cidade;
    }

}
