package ClassesObjetos;

public class Garagem {
    int numeroCarros;
    boolean coberta;
    String porta;

    public Garagem(){}
    
    
    public Garagem(int numeroCarros, boolean coberta, String porta){
        this.numeroCarros = numeroCarros;
        this.coberta = coberta;
        this.porta = porta;
    }
}
