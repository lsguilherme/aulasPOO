package IntroducaoPOO;

// Qual a diferença entre o JDK, JVM e JRE?

/**
 * JDK(Java Development Kit) - É o kit de desenvolvimento do Java, feito especialmente para desenvolver aplicações na linguagem Java. Formado pela JRE e algumas ferramentas, como o compilador. 
 * JVM(Java Virtual Machine) - A virtual machine do java ela permite que um único código seja feito para diversos sistemas operacionais. Recebe o código compilado em bytecodes e "traduz" para linguagem da máquina nativa.
 * JRE(Java Runtime Environment) - Ambiente de execução do Java, é formado pela JVM e bibliotecas. Tem a função de executar aplicações Java.
 */